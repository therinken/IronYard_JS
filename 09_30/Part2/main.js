var matt=[240, 210.4, 235.1, 208.9, 197.5], thisTotal=0,thisAverage=0;
// add elements of array together
 for(var i=0;i<matt.length;i++)
  {thisTotal+=matt[i];}
// calculate average
 thisAverage=(thisTotal/matt.length);
// display result
 console.log("Matt's overall marathon average is " + thisAverage.toFixed(2) + " minutes!");

